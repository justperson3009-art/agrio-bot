from aiogram import Router, F
from aiogram.types import Message, FSInputFile
from aiogram.fsm.context import FSMContext

router = Router()

@router.message(F.photo)
async def handle_photo(message: Message):
    """Обработка фото растения"""
    caption = (message.caption or "").lower()
    
    if not caption:
        await message.answer(
            "📸 **Фото получено!**\n\n"
            "Напишите, что вас беспокоит:\n"
            "• Жёлтые листья\n"
            "• Пятна на листьях\n"
            "• Увядание\n"
            "• Вредители\n"
            "• Другое\n\n"
            "💡 Или опишите симптомы словами"
        )
        return
    
    # Анализируем описание к фото
    diagnosis = _diagnose_from_caption(caption)
    await message.answer(diagnosis)

def _diagnose_from_caption(caption: str) -> str:
    """Диагностика по описанию к фото"""
    
    # Фитофтора
    if any(w in caption for w in ['чёрн', 'коричнев', 'фитофтор', 'тёмные пятна']):
        if any(w in caption for w in ['томат', 'помидор', 'картоф']):
            return (
                "🔍 **ДИАГНОЗ: Фитофтора**\n\n"
                "📋 **Признаки:** Чёрные/коричневые пятна на листья и плодах\n"
                "🧪 **Лечение:**\n"
                "• Удалить поражённые части\n"
                "• Обработать: Ридомил Голд, ОксиХОМ\n"
                "• Народное: чеснок (200г/10л) + марганцовка\n\n"
                "🛡️ **Профилактика:** проветривание, не загущать"
            )
    
    # Мучнистая роса
    if any(w in caption for w in ['белый налёт', 'белый налет', 'мучнист', 'пудр']):
        return (
            "🔍 **ДИАГНОЗ: Мучнистая роса**\n\n"
            "📋 **Признаки:** Белый мучнистый налёт на листьях\n"
            "🧪 **Лечение:**\n"
            "• Топаз, Фитоспорин\n"
            "• Сыворотка 1:10 (опрыскивание)\n\n"
            "🛡️ **Профилактика:** вентиляция, не мочить листья"
        )
    
    # Тля
    if any(w in caption for w in ['тля', 'мелкие зелёные', 'мелкие зеленые', 'скручив', 'мелкие насеком']):
        return (
            "🔍 **ДИАГНОЗ: Тля**\n\n"
            "📋 **Признаки:** Мелкие насекомие на нижней стороне листьев, скрученные листья\n"
            "🧪 **Лечение:**\n"
            "• Фитоверм (био)\n"
            "• Мыльный раствор (100г/10л)\n"
            "• Настой чеснока (200г/10л)\n\n"
            "🛡️ **Профилактика:** божьи коровки, удаление сорняков"
        )
    
    # Желтеют листья - нехватка азота
    if any(w in caption for w in ['желтеют', 'жёлтые', 'желт', 'бледные']):
        if any(w in caption for w in ['нижн', 'стар']):
            return (
                "🔍 **ДИАГНОЗ: Нехватка азота**\n\n"
                "📋 **Признаки:** Жёлтые нижние листья\n"
                "🧪 **Лечение:**\n"
                "• Мочевина 1 ст.л./10л\n"
                "• Настой крапивы 1:10\n\n"
                "💡 Азот вымывается при частом поливе"
            )
        return (
            "🔍 **ВОЗМОЖНЫЕ ПРИЧИНЫ ПОЖЕЛТЕНИЯ:**\n\n"
            "1️⃣ Нехватка азота → подкормить\n"
            "2️⃣ Перелив → дать просохнуть\n"
            "3️⃣ Корневая гниль → Фитоспорин\n"
            "4️⃣ Фитофтора → удалить части, обработать\n\n"
            "💡 Опишите точнее — дам точный диагноз!"
        )
    
    # Увядание
    if any(w in caption for w in ['увяд', 'вянет', 'повис', 'опуска']):
        return (
            "🔍 **ВОЗМОЖНЫЕ ПРИЧИНЫ УВЯДАНИЯ:**\n\n"
            "1️⃣ Недолив → полить!\n"
            "2️⃣ Перелив → проверить корни на гниль\n"
            "3️⃣ Фузариоз → Фитоспорин под корень\n"
            "4️⃣ Жара → притенить, опрыскать\n\n"
            "💡 Проверьте влажность почвы!"
        )
    
    # Паутинный клещ
    if any(w in caption for w in ['паутин', 'клещ', 'мелкие точк', 'сохнут листья']):
        return (
            "🔍 **ДИАГНОЗ: Паутинный клещ**\n\n"
            "📋 **Признаки:** Паутина на листьях, мелкие точки, листья сохнут\n"
            "🧪 **Лечение:**\n"
            "• Фитоверм, Актофит\n"
            "• Повысить влажность\n"
            "• Удалить поражённые листья\n\n"
            "🛡️ **Профилактика:** регулярный осмотр, влажность"
        )
    
    # По умолчанию
    return (
        f"📸 **Фото получено!**\n\n"
        f"Описание: \"{message.caption[:100]}\"\n\n"
        "Я не смог определить болезнь точно. Попробуйте описать симптомы:\n"
        "• Цвет пятен\n"
        "• Где находятся (нижние/верхние листья)\n"
        "• Есть ли налёт\n"
        "• Видны ли насекомые\n\n"
        "💡 Или отправьте фото другого растения"
    )
