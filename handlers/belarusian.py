@router.message(F.text)
async def handle_belarusian(message: Message):
    """Обработка сообщений на белорусском"""
    text = message.text.lower()
    
    # Проверяем белорусские слова
    by_words = ['калі', 'як', 'што', 'дзякуй', 'прывітанне', 'бульба', 'тамат', 'агурок']
    
    if any(w in text for w in by_words):
        # Ищем лучший ответ
        for key, response in BY_RESPONSES.items():
            if key in text:
                await message.answer(response)
                return
        
        # Общий ответ
        await message.answer(
            "🌱 **Добры дзень!**\n\n"
            "Я разумею беларускую мову!\n"
            "Напішыце культуру або пытанне — дапамагу.\n\n"
            "💡 Можна таксама па-руску"
        )
