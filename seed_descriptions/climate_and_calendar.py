def get_planting_calendar(month: str = None) -> str:
    """
    Получить календарь посадок.

    Args:
        month: Название месяца (если None — текущий месяц)

    Returns:
        Строка с календарём
    """
    from datetime import datetime

    month_names = {
        "январь": "январь", "февраль": "февраль", "март": "март",
        "апрель": "апрель", "май": "май", "июнь": "июнь",
        "июль": "июль", "август": "август", "сентябрь": "сентябрь",
        "октябрь": "октябрь", "ноябрь": "ноябрь", "декабрь": "декабрь",
    }

    # Синонимы
    synonyms = {
        "янв": "январь", "фев": "февраль", "мар": "март",
        "апр": "апрель", "июн": "июнь", "июл": "июль",
        "авг": "август", "сен": "сентябрь", "окт": "октябрь",
        "ноя": "ноябрь", "дек": "декабрь",
    }

    if month:
        month_lower = month.lower().strip()
        month_lower = synonyms.get(month_lower, month_lower)
    else:
        # Текущий месяц
        month_lower = datetime.now().strftime("%B").lower()
        month_lower = month_lower.replace("января", "январь").replace("февраля", "февраль")
        month_lower = month_lower.replace("марта", "март").replace("апреля", "апрель")
        month_lower = month_lower.replace("мая", "май").replace("июня", "июнь")
        month_lower = month_lower.replace("июля", "июль").replace("августа", "август")
        month_lower = month_lower.replace("сентября", "сентябрь").replace("октября", "октябрь")
        month_lower = month_lower.replace("ноября", "ноябрь").replace("декабря", "декабрь")

    if month_lower in PLANTING_CALENDAR:
        cal = PLANTING_CALENDAR[month_lower]
        result = f"📅 **КАЛЕНДАРЬ ПОСАДОК: {cal['month'].upper()}**\n\n"
        result += f"📌 {cal['description']}\n\n"

        if cal["sow"]:
            result += "🌱 **Что сажать:**\n"
            for item in cal["sow"]:
                result += f"• {item}\n"
            result += "\n"

        if cal["care"]:
            result += "🔧 **Что делать:**\n"
            for item in cal["care"]:
                result += f"• {item}\n"
            result += "\n"

        if cal["harvest"]:
            result += "🧺 **Что убирать:**\n"
            for item in cal["harvest"]:
                result += f"• {item}\n"
            result += "\n"

        result += "💡 *Уточните культуру — дам подробные рекомендации!*"

        return result

    return None


def get_all_months() -> list:
    """Получить список всех месяцев"""
    return list(PLANTING_CALENDAR.keys())
