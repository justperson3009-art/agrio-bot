def get_climate_info(country: str) -> str:
    """
    Получить информацию о климате страны.

    Args:
        country: Название страны

    Returns:
        Строка с информацией о климате или None
    """
    country_lower = country.lower().strip()

    # Синонимы
    synonyms = {
        "рф": "россия",
        "российская федерация": "россия",
        "беларуси": "беларусь",
        "белоруссия": "беларусь",
        "белоруссии": "беларусь",
        "украины": "украина",
        "казахстана": "казахстан",
        "узбекистана": "узбекистан",
        "азербайджана": "азербайджан",
        "кыргызстана": "кыргызстан",
        "киргизии": "кыргызстан",
        "таджикистана": "таджикистан",
        "туркменистана": "туркменистан",
        "армении": "армения",
        "грузии": "грузия",
        "молдовы": "молдова",
        "молдавии": "молдова",
    }

    country_normalized = synonyms.get(country_lower, country_lower)

    if country_normalized in CLIMATE_INFO:
        country_data = CLIMATE_INFO[country_normalized]
        result = f"🌍 **КЛИМАТ: {country_data['name']}**\n\n"

        for region_key, region_data in country_data["regions"].items():
            result += f"📍 **{region_data['name']}**\n"
            result += f"📌 {region_data['description']}\n\n"
            result += f"🌡️ Климат: {region_data['climate']}\n"
            result += f"❄️ Зима: {region_data['winter_temp']}\n"
            result += f"☀️ Лето: {region_data['summer_temp']}\n"
            result += f"📅 Без заморозков: {region_data['frost_free_days']}\n"
            result += f"🌱 Сезон посадок: {region_data['planting_season']}\n\n"

            result += "✨ **Особенности:**\n"
            for feature in region_data["features"]:
                result += f"• {feature}\n"
            result += "\n"

        return result

    return None


def get_all_countries() -> list:
    """Получить список всех стран"""
    return list(CLIMATE_INFO.keys())


