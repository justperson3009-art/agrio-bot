def get_crop_guide(crop_name: str) -> str | None:
    """
    Получить универсальное руководство по культуре.

    Args:
        crop_name: Название культуры (томат, перец, огурец и т.д.)

    Returns:
        Строка с руководством или None
    """
    crop_lower = crop_name.lower().strip()

    # Словарь синонимов
    synonyms = {
        "помидор": "томат",
        "помидоры": "томат",
        "томаты": "томат",
        "перцы": "перец",
        "синенькие": "баклажан",
        "баклажаны": "баклажан",
        "капусты": "капуста",
        "огурцы": "огурец",
        "кабачки": "кабачок",
        "тыквы": "тыква",
        "арбузы": "арбуз",
        "дыни": "дыня",
        "морковки": "морковь",
        "фасоли": "фасоль",
        "кукурузы": "кукуруза",
        "брокколи": "брокколи",
        "петрушки": "петрушка",
        "редиски": "редис",
        "свёкла": "свекла",
        "свеклы": "свекла",
    }

    # Нормализуем название
    crop_normalized = synonyms.get(crop_lower, crop_lower)

    if crop_normalized in CROP_GUIDES:
        guide = CROP_GUIDES[crop_normalized]
        result = guide["general_guide"]
        result += "\n\n" + guide["region_info"]
        result += "\n\n💡 *Уточните сорт — расскажу подробнее о конкретном гибриде!*"
        return result

    return None


def get_all_crops() -> list:
    """Получить список всех культур"""
    return list(CROP_GUIDES.keys())
